export const getColor = <
  TCategory extends keyof ThemeColors,
  TShade extends TCategory extends 'special'
  ? keyof SpecialCategory
  : ColorShade
>(
  category: TCategory,
  shadeOrName: TShade,
  theme: 'light' | 'dark' = 'dark'
): string => {
  const colorSet = colors[theme][category];
  return (colorSet[shadeOrName as unknown as keyof typeof colorSet]) as string;
};

export const getRGBColor = <
  TCategory extends keyof ThemeColors,
  TShade extends TCategory extends 'special'
  ? keyof SpecialCategory
  : ColorShade
>(
  category: TCategory,
  shadeOrName: TShade,
  theme: 'light' | 'dark' = 'dark'
): string => {
  const colorSet = getColor(category, shadeOrName, theme);
  return `rgb(${colorSet})`;
};